function sum (num1, num2) {
    let result = num1 + num2;
    return result;
}

console.log(sum(2, 3));

function subt (num1, num2) {
    let result = num1 - num2;
    return result;
}

console.log(subt(3, 2));

function mult (num1, num2) {
    let result = num1 * num2;
    return result;
}

console.log(mult(2, 3));

function div (num1, num2) {
    let result = num1 / num2;
    return result;
}

console.log(div(6, 3));

function mod (num1, num2) {
    let result = num1 % num2;
    return result;
}

console.log(mod(6, 2));
